double PSaid::dsdw(double th, double tlab) {
    // normalized pp elastic differential cross sections (mb/sr)
    // arguments: scattering angle (deg), lab beam kinetic energy (GeV)

    double a=th/90.;
    double costh=cos(th*d2r); 
    double s2=1.-costh*costh;
    double sinth = sqrt(s2);
    double *d=(double*)PP;
    if (a>1.) a=2.-a;
    double ze=pow(tlab/emx,pei);
    double za=pow(a,pai);
    int ie=int(ze/dez);
    int ia=int(za/daz);
    if (ie<1) ie=1;
    else if (ie>nez-1) ie=nez-1;
    if (ia<1) ia=1;
    else if (ia>naz-1) ia=naz-1;
    int iam=ia-1;
    int iem=ie-1;
    double d0=d[iam+iem*naz];
    a=(d[ia+iem*naz]-d0)/daz;
    double b=(d[iam+ie*naz]-d0)/dez;
    double ab=(d[ia+ie*naz]-a*daz-b*dez-d0)/(daz*dez);
    ze-=dez*ie;
    za-=daz*ia;
    return sinth*(d0+a*za+b*ze+ab*za*ze)/(s2*s2*tlab);
}

double PSaid::sample(double r, double tlab) {
    const double fac=1.1;
    int i, j;
    // _____________________________________________________________________
    // enter whenever beam energy changes, to calculate integrals and slopes
    if (told!=tlab) {
	told=tlab;
	double sum=0.;
	for (i=0;i<=dim;++i) y[i]=fac*dsdw((double)(i+1),tlab);
	for (i=0;i<dim;++i) {
	    sum += (y[i+1]>y[i]) ? y[i+1] : y[i] ;
	    aa[i]=sum;
	}
    }
  
    // _____________________________________________________________________
    // sample cm scattering angle with the rejection method
    double rr, f, tf, r2, c0, r1=r;
 repeat:
    i=(r1>.5);
    rr=(2.*r1-i)*aa[dim-1];
    for (j=0;j<dim;++j) {if (rr<aa[j]) break;}
    if (j) rr-=aa[j-1];
    tf = (y[j+1]>y[j]) ? y[j+1]: y[j];
    c0=rr/tf+1.+j;
    f=dsdw(c0,tlab);
    //r2=REngine->Rndm();
    r2=PUtils::sampleFlat();
    if (r2>f/tf) goto fail;
    if (f<=tf) return (1-2*i)*cos(c0*d2r);

    Warning("sample","Sampling failed");
    return -99999.;
 fail:
    //r1=REngine->Rndm();
    r1=PUtils::sampleFlat();
    goto repeat;
}


